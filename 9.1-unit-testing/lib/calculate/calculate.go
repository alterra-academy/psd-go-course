package calculate

func Additon(a, b int) int {
  result := a + b
  return result
}

func Substract(number_a, number_b int) int {
	result := number_a - number_b
	return result
}

func Mult(number_a, number_b int) int {
  result := number_a * number_b
  return result
}

func Div(number_a, number_b int) int {
  result := number_a / number_b
  return result
}
